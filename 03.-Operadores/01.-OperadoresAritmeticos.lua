-- Operaciones Artimeticos

-- Suma
local a = 3 + 4
print("Suma:", a)

-- Resta
local b = 6 - 2
print("Resta:", b)

-- Multiplicación
local c = a * 2
print("Multiplicación:", c)

-- Division
local d = b / 2.5
print("División:", d)

-- División Entera
local d = b // 2.5
print("División entera:", d)

-- Modulo
local f = 9 % 2
print("Modulo:", f)

local e = a ^ 2
print("Potencia:", e)