-- No Existen Operadores de asignaci√≥n
-- Debes hacer por separado

local mi_numero = 20
print(mi_numero)

-- Operador +=
mi_numero = mi_numero + 5
print(mi_numero)

-- Operador -=
mi_numero = mi_numero - 3
print(mi_numero)


-- Operador *=
mi_numero = mi_numero * 2
print(mi_numero)

-- Operador /=
mi_numero = mi_numero / 4
print(mi_numero)

-- Operador %=
mi_numero = mi_numero % 2
print(mi_numero)

-- Operador ^=
mi_numero = 2
mi_numero = mi_numero ^ 3
print(mi_numero)